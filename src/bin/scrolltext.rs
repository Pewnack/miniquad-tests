//! Scrolltext Test
//!
//! Renders white text that scrolls smoothly from left to right across a black
//! background.  When the text has fully passed the right edge it loops back
//! from the left edge.
//!
//! **What it tests:**
//! - Texture creation from raw RGBA8 pixel data
//! - Smooth scrolling animation via per-frame vertex buffer updates
//!   (`BufferUsage::Stream`)
//! - Textured-quad rendering with UV coordinates
//! - Delta-time animation using `miniquad::date::now()`
//!
//! **How to use:**
//! Run with: `cargo run --bin scrolltext`
//! You should see white text scrolling left to right on a black background.

