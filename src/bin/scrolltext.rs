/// Build an RGBA8 texture image for `text`.
/// Output dimensions: `(text.len() * 8)` × `8` pixels.
/// White pixels for lit bits, black (opaque) for unlit bits.
fn build_text_texture(text: &str) -> (Vec<u8>, u16, u16) {
    let tex_w = text.len() * 8;
    let tex_h = 8_usize;
    let mut pixels = vec![0u8; tex_w * tex_h * 4];

    for (ci, ch) in text.chars().enumerate() {
        let code = ch as usize;
        if code < 32 || code > 126 {
            continue;
        }
        let glyph = &FONT[code - 32];
        for row in 0..8usize {
            let byte = glyph[row];
            for col in 0..8usize {
                // In font8x8 bit-0 is the leftmost pixel.
                let on = (byte >> col) & 1 != 0;
                let px = ci * 8 + col;
                let py = row;
                let idx = (py * tex_w + px) * 4;
                let v: u8 = if on { 255 } else { 0 };
                pixels[idx] = v;
                pixels[idx + 1] = v;
                pixels[idx + 2] = v;
                pixels[idx + 3] = 255;
            }
        }
    }

    (pixels, tex_w as u16, tex_h as u16)
}

// ── Vertex ────────────────────────────────────────────────────────────────────

#[repr(C)]
struct Vertex {
    pos: [f32; 2],
    uv: [f32; 2],
}

/// Build 4 vertices for the text quad given the left-edge pixel position.
fn make_vertices(scroll_x: f32, text_px_w: f32) -> [Vertex; 4] {
    let char_h_px = 8.0 * SCALE;
    let top_py = WIN_H / 2.0 - char_h_px / 2.0;
    let bot_py = WIN_H / 2.0 + char_h_px / 2.0;

    // Convert pixel coordinates to NDC (y=0 is top of window).
    let x0 = scroll_x / WIN_W * 2.0 - 1.0;
    let x1 = (scroll_x + text_px_w) / WIN_W * 2.0 - 1.0;
    let y0 = 1.0 - top_py / WIN_H * 2.0; // top
    let y1 = 1.0 - bot_py / WIN_H * 2.0; // bottom

    [
        Vertex { pos: [x0, y0], uv: [0.0, 0.0] }, // top-left
        Vertex { pos: [x1, y0], uv: [1.0, 0.0] }, // top-right
        Vertex { pos: [x1, y1], uv: [1.0, 1.0] }, // bottom-right
        Vertex { pos: [x0, y1], uv: [0.0, 1.0] }, // bottom-left
    ]
}

// ── Stage ─────────────────────────────────────────────────────────────────────

struct Stage {
    ctx: Box<dyn RenderingBackend>,
    pipeline: Pipeline,
    bindings: Bindings,
    /// Left-edge position of the text in screen pixels.
    scroll_x: f32,
    /// Total width of the rendered text in screen pixels.
    text_px_w: f32,
    /// Wall-clock time (seconds) of the previous frame, for delta-time.
    last_time: f64,
}

impl Stage {
    fn new() -> Stage {
        let mut ctx = window::new_rendering_backend();

        // ── Build text texture ────────────────────────────────────────────────
        let (pixels, tex_w, tex_h) = build_text_texture(TEXT);
        let texture = ctx.new_texture_from_rgba8(tex_w, tex_h, &pixels);
        ctx.texture_set_filter(texture, FilterMode::Nearest, MipmapFilterMode::None);

        // ── Geometry ─────────────────────────────────────────────────────────
        let text_px_w = TEXT.len() as f32 * 8.0 * SCALE;
        // Start with text fully off the left edge.
        let initial_x = -text_px_w;
        let vertices = make_vertices(initial_x, text_px_w);

        let vertex_buffer = ctx.new_buffer(
            BufferType::VertexBuffer,
            BufferUsage::Stream,
            BufferSource::slice(&vertices),
        );
        let index_buffer = ctx.new_buffer(
            BufferType::IndexBuffer,
            BufferUsage::Immutable,
            BufferSource::slice(&[0u16, 1, 2, 0, 2, 3]),
        );

        let bindings = Bindings {
            vertex_buffers: vec![vertex_buffer],
            index_buffer,
            images: vec![texture],
        };

        // ── Shader ────────────────────────────────────────────────────────────
        let shader = ctx
            .new_shader(
                match ctx.info().backend {
                    Backend::OpenGl => ShaderSource::Glsl {
                        vertex: shader::VERTEX,
                        fragment: shader::FRAGMENT,
                    },
                    Backend::Metal => ShaderSource::Msl { program: shader::METAL },
                },
                shader::meta(),
            )
            .unwrap();

        let pipeline = ctx.new_pipeline(
            &[BufferLayout::default()],
            &[
                VertexAttribute::new("pos", VertexFormat::Float2),
                VertexAttribute::new("uv", VertexFormat::Float2),
            ],
            shader,
            PipelineParams::default(),
        );

        Stage {
            ctx,
            pipeline,
            bindings,
            scroll_x: initial_x,
            text_px_w,
            last_time: miniquad::date::now(),
        }
    }
}

impl EventHandler for Stage {
    fn update(&mut self) {
        let now = miniquad::date::now();
        let dt = (now - self.last_time) as f32;
        self.last_time = now;

        self.scroll_x += SCROLL_SPEED * dt;
        // When the text has fully scrolled past the right edge, loop back.
        if self.scroll_x > WIN_W {
            self.scroll_x = -self.text_px_w;
        }
    }

    fn draw(&mut self) {
        // Upload updated quad position.
        let vertices = make_vertices(self.scroll_x, self.text_px_w);
        self.ctx.buffer_update(
            self.bindings.vertex_buffers[0],
            BufferSource::slice(&vertices),
        );

        self.ctx.begin_default_pass(PassAction::clear_color(0.0, 0.0, 0.0, 1.0));
        self.ctx.apply_pipeline(&self.pipeline);
        self.ctx.apply_bindings(&self.bindings);
        self.ctx.draw(0, 6, 1);
        self.ctx.end_render_pass();
        self.ctx.commit_frame();
    }
}

fn main() {
    let conf = create_test_window_conf();
    miniquad::start(conf, || Box::new(Stage::new()));
}

