//! Files-Dropped Event Test
//!
//! Demonstrates the `files_dropped_event` callback in miniquad.
//!
//! **What it tests:**
//! - `files_dropped_event` — fires whenever the OS delivers one or more files
//!   to the window via a drag-and-drop gesture.
//! - `window::dropped_file_count` / `window::dropped_file_path` — runtime API
//!   for querying the paths of the just-dropped files.
//! - Dynamic texture creation from text rendered with a bitmap font.
//!
//! **How to use:**
//! Run with: `cargo run --bin files_dropped`
//! Drag one or more files onto the window.  The paths of the dropped files are
//! displayed in white on a black background.  Each new drop clears the previous
//! list and shows only the most recently dropped paths.

