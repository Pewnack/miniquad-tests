/// Build an RGBA8 texture image for `text`.
/// Output dimensions: `(text.len() * 8)` × `8` pixels.
/// White pixels for lit bits, transparent black for unlit bits.
fn build_text_texture(text: &str) -> (Vec<u8>, u16, u16) {
    let tex_w = text.len() * 8;
    let tex_h = 8_usize;
    let mut pixels = vec![0u8; tex_w * tex_h * 4];

    for (ci, ch) in text.chars().enumerate() {
        let code = ch as usize;
        if code < 32 || code > 126 {
            continue;
        }
        let glyph = &FONT[code - 32];
        for row in 0..8usize {
            let byte = glyph[row];
            for col in 0..8usize {
                let on = (byte >> col) & 1 != 0;
                if on {
                    let px = ci * 8 + col;
                    let py = row;
                    let idx = (py * tex_w + px) * 4;
                    pixels[idx] = 255;
                    pixels[idx + 1] = 255;
                    pixels[idx + 2] = 255;
                    pixels[idx + 3] = 255;
                }
            }
        }
    }

    (pixels, tex_w as u16, tex_h as u16)
}

// ── Vertex ────────────────────────────────────────────────────────────────────

#[repr(C)]
struct Vertex {
    pos: [f32; 2],
    uv: [f32; 2],
}

/// Build 4 vertices that place `text_px_w` × `glyph_h_px` text starting at
/// `(left_px, top_px)` in window-pixel coordinates.
fn make_quad(left_px: f32, top_px: f32, text_px_w: f32) -> [Vertex; 4] {
    let glyph_h_px = 8.0 * SCALE;

    let x0 = left_px / WIN_W * 2.0 - 1.0;
    let x1 = (left_px + text_px_w) / WIN_W * 2.0 - 1.0;
    let y0 = 1.0 - top_px / WIN_H * 2.0;
    let y1 = 1.0 - (top_px + glyph_h_px) / WIN_H * 2.0;

    [
        Vertex { pos: [x0, y0], uv: [0.0, 0.0] },
        Vertex { pos: [x1, y0], uv: [1.0, 0.0] },
        Vertex { pos: [x1, y1], uv: [1.0, 1.0] },
        Vertex { pos: [x0, y1], uv: [0.0, 1.0] },
    ]
}

// ── Per-line rendering data ───────────────────────────────────────────────────

struct TextLine {
    bindings: Bindings,
}

// ── Stage ─────────────────────────────────────────────────────────────────────

struct Stage {
    ctx: Box<dyn RenderingBackend>,
    pipeline: Pipeline,
    index_buffer: BufferId,
    lines: Vec<TextLine>,
    /// True if the display needs to be rebuilt from `pending_paths`.
    dirty: bool,
    pending_paths: Vec<String>,
}

impl Stage {
    fn new() -> Stage {
        let mut ctx = window::new_rendering_backend();

        let index_buffer = ctx.new_buffer(
            BufferType::IndexBuffer,
            BufferUsage::Immutable,
            BufferSource::slice(&[0u16, 1, 2, 0, 2, 3]),
        );

        let shader = ctx
            .new_shader(
                match ctx.info().backend {
                    Backend::OpenGl => ShaderSource::Glsl {
                        vertex: shader::VERTEX,
                        fragment: shader::FRAGMENT,
                    },
                    Backend::Metal => ShaderSource::Msl { program: shader::METAL },
                },
                shader::meta(),
            )
            .unwrap();

        let pipeline = ctx.new_pipeline(
            &[BufferLayout::default()],
            &[
                VertexAttribute::new("pos", VertexFormat::Float2),
                VertexAttribute::new("uv", VertexFormat::Float2),
            ],
            shader,
            PipelineParams {
                color_blend: Some(BlendState::new(
                    Equation::Add,
                    BlendFactor::Value(BlendValue::SourceAlpha),
                    BlendFactor::OneMinusValue(BlendValue::SourceAlpha),
                )),
                ..Default::default()
            },
        );

        // Show initial prompt as a line of text.
        let prompt = "Drop files here...";
        let initial_lines = vec![prompt.to_string()];

        let lines = Stage::build_lines(&mut ctx, index_buffer, &initial_lines);

        Stage {
            ctx,
            pipeline,
            index_buffer,
            lines,
            dirty: false,
            pending_paths: Vec::new(),
        }
    }

    /// Create `TextLine` entries for each string, laid out from the top of the window.
    fn build_lines(
        ctx: &mut Box<dyn RenderingBackend>,
        index_buffer: BufferId,
        texts: &[String],
    ) -> Vec<TextLine> {
        let glyph_h_px = 8.0 * SCALE;
        let margin = 20.0;
        let mut result = Vec::with_capacity(texts.len());

        for (i, text) in texts.iter().enumerate() {
            // Replace non-printable characters (outside ASCII 0x20–0x7E) with '?'.
            let safe: String = text
                .chars()
                .map(|c| if (0x20..=0x7E).contains(&(c as u32)) { c } else { '?' })
                .collect();

            let (pixels, tex_w, tex_h) = build_text_texture(&safe);
            let texture = ctx.new_texture_from_rgba8(tex_w, tex_h, &pixels);
            ctx.texture_set_filter(texture, FilterMode::Nearest, MipmapFilterMode::None);

            let text_px_w = safe.len() as f32 * 8.0 * SCALE;
            let top_px = margin + i as f32 * (glyph_h_px + LINE_GAP);

            let vertices = make_quad(margin, top_px, text_px_w);            let vertex_buffer = ctx.new_buffer(
                BufferType::VertexBuffer,
                BufferUsage::Immutable,
                BufferSource::slice(&vertices),
            );

            result.push(TextLine {
                bindings: Bindings {
                    vertex_buffers: vec![vertex_buffer],
                    index_buffer,
                    images: vec![texture],
                },
            });
        }

        result
    }

    /// Delete GPU resources for the current lines and rebuild from `pending_paths`.
    fn rebuild_lines(&mut self) {
        // Free old textures and vertex buffers.
        for line in self.lines.drain(..) {
            self.ctx.delete_texture(line.bindings.images[0]);
            self.ctx.delete_buffer(line.bindings.vertex_buffers[0]);
        }

        let texts = std::mem::take(&mut self.pending_paths);
        self.lines = Stage::build_lines(&mut self.ctx, self.index_buffer, &texts);
        self.dirty = false;
    }
}

impl EventHandler for Stage {
    fn update(&mut self) {
        if self.dirty {
            self.rebuild_lines();
        }
    }

    fn draw(&mut self) {
        self.ctx.begin_default_pass(PassAction::clear_color(0.0, 0.0, 0.0, 1.0));
        self.ctx.apply_pipeline(&self.pipeline);

        for line in &self.lines {
            self.ctx.apply_bindings(&line.bindings);
            self.ctx.draw(0, 6, 1);
        }

        self.ctx.end_render_pass();
        self.ctx.commit_frame();
    }

    fn files_dropped_event(&mut self) {
        let count = window::dropped_file_count();
        self.pending_paths = (0..count)
            .filter_map(|i| window::dropped_file_path(i))
            .map(|p| p.to_string_lossy().into_owned())
            .collect();
        self.dirty = true;
    }
}

fn main() {
    let conf = create_test_window_conf();
    miniquad::start(conf, || Box::new(Stage::new()));
}

